# Step 1: Ask the user how old they are now
current_age = input("How old are you? ")

# Step 2: Turn the input into a number
current_age = int(current_age)

# Step 3: Add 27 years to find their age in 2050
future_age = current_age + 27

# Step 4: Tell them how old they’ll be in 2050
print("In 2050, you will be", future_age, "years old.")
